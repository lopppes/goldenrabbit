// função para animar os elementos ao rolar a página
function animateOnScroll() {
  var elements = document.querySelectorAll('.fade-in'); // Seleciona todos os elementos com a classe 'fade-in'

  elements.forEach(function(element) {
    if (isElementInViewport(element)) {
      element.classList.add('active');
    } else {
      element.classList.remove('active');
    }
  });
}

// função para verificar se um elemento está visível na tela
function isElementInViewport(element) {
  var rect = element.getBoundingClientRect();
  var windowHeight = window.innerHeight || document.documentElement.clientHeight;

  // verifica se o elemento está mais de 20% visível na tela
  return (
    rect.top <= windowHeight * 0.9
  );
}

// adicionar o evento de scroll à janela
window.addEventListener('scroll', animateOnScroll);

// função para animar os elementos visíveis inicialmente
animateOnScroll();


